package co.siten.myvtg.model.subInforWS;

/**
 * ImageCus
 *
 * @author partner7
 */
public class ImageCus {

    private String name;
    private String data;

    public ImageCus() {
    }

    public ImageCus(String name, String data) {
        this.name = name;
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

}
